package tests

import (
	"testing"

	"github.com/Dedalum/goatter/blockchain"
	"github.com/stretchr/testify/assert"
)

func TestHashTransactions(t *testing.T) {

	address := "test address"
	expectedHash := []byte{0x0, 0x1e, 0x89, 0x7d, 0xbc, 0xf9, 0x32, 0x9e, 0x5a, 0x82, 0x64, 0xb3, 0x8d, 0xe7, 0x1c, 0x5, 0xe1, 0xd0, 0x8e, 0x77, 0x55, 0x32, 0x6e, 0x64, 0x3, 0xa4, 0x58, 0x96, 0xa6, 0xfb, 0xa4, 0x98}
	coinbase := blockchain.CoinbaseTx(address, "Test transaction")
	b := blockchain.CreateBlock([]*blockchain.Transaction{coinbase}, []byte{})
	b.HashTransactions()
	assert.Equal(t, b.Hash, expectedHash)
}
